function toggleDescription(id) {
  var descripcion = document.getElementById('descripcion_' + id);
  var descripcionCompleta = document.getElementById('descripcion_full_' + id);
  var verMenos = document.getElementById('vermenosDescription_' + id);

  if (descripcion.style.display === 'none') {
    descripcion.style.display = 'inline';
    descripcionCompleta.style.display = 'none';
    verMenos.style.display = 'none';
  } else {
    descripcion.style.display = 'none';
    descripcionCompleta.style.display = 'inline';
    verMenos.style.display = 'inline';
  }
}

